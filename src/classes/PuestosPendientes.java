package classes;

/**
 *
 * @author Jorge
 */
public class PuestosPendientes {
        String id,producto,rfc,movimiento,fecha,puesto;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getProducto() {
        return producto;
    }

    public void setProducto(String producto) {
        this.producto = producto;
    }

    public String getRfc() {
        return rfc;
    }

    public void setRfc(String rfc) {
        this.rfc = rfc;
    }

    public String getMovimiento() {
        return movimiento;
    }

    public void setMovimiento(String movimiento) {
        this.movimiento = movimiento;
    }

    public String getFecha() {
        return fecha;
    }

    public void setFecha(String fecha) {
        this.fecha = fecha;
    }

    public String getPuesto() {
        return puesto;
    }

    public void setPuesto(String puesto) {
        this.puesto = puesto;
    }

    public PuestosPendientes(String id, String producto, String rfc, String movimiento, String fecha, String puesto) {
        this.id = id;
        this.producto = producto;
        this.rfc = rfc;
        this.movimiento = movimiento;
        this.fecha = fecha;
        this.puesto = puesto;
    }
}
